package Basics;

//looping Statement in java
//1.for loop
//3.do while loop
//4.for each loop
public class LoopingStatement {
	public static void main(String[] args) {
		
		//print 0 to 20 value using for loop
		for(int i=0;i<=20;i++)
		{
			System.out.println(i);
		}
		
		//print 0 to 20 value using while loop
		int j=0;
		while(j<=20)
		{
			System.out.println(j);
			j++;
		}
		//print 0 to 20 value using do while loop 
		int k=0;
		do
		{
			System.out.println(k);
			k++;
			
		}
		while (k<=20);
	}
	

}
